const addTracking = () => {

  document.body.addEventListener('click', (e) => {

    if (e.target.id == "add2CartBtn") {
      fireEvent('Click - add to basket button is clicked', true);
    }

    if (VARIATION == 1 && (e.target.classList.contains(`${ID}-bubble-nav-link`) || e.target.closest(`.${ID}-bubble-nav-link`))) {
      let target = e.target.getAttribute('href');
      let typeClicked = "";
      if(target.indexOf('/eyes/') > -1) {
        typeClicked = "eyes";
      } else if(target.indexOf('/lips/') > -1) {
        typeClicked = "lips";
      }
      fireEvent(`Click - bubble navigation link is clicked, from the ${typeClicked} category, going to href: ${target}`, true);
    }

  });

}

