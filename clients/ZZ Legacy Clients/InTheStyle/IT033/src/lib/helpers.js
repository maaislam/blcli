import UC from './UC.js';

export const fullStory = function (experiment, variation) {
    // Full Story Tagging --------------------------
    // ---------------------------------------------
    UC.poller([
        function () {
            var fs = window.FS;
            if (fs && fs.setUserVars) return true;
        }
    ], function () {
        window.FS.setUserVars({
            experiment_str: experiment,
            variation_str: variation
        });
    }, {multiplier: 1.2, timeout: 0});

}; // fullStory

