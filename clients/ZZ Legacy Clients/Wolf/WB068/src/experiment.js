    function assignClothingSizes() {
        $('.size-guide').text('View full size guide');

        if (innerCategory == "trousers" || innerCategory == "trousers & shorts") {
            if (maleFemale == "men") {
                menTrousers.appendTo('.variant-row');
            } else {
                womenTrousers.appendTo('.variant-row');
            }

        } else if (innerCategory == "shirts") {
            menShirts.appendTo('.variant-row');

        } else if (innerCategory == "rings") {
            if (maleFemale == "men") {
                menRings.appendTo('.variant-row');
            } else {
                womenRings.appendTo('.variant-row');
            }

        } else if (innerCategory == "shoes") {
            if (maleFemale == "men") {
                menShoes.appendTo('.variant-row');
            } else {
                womenShoes.appendTo('.variant-row');
            }

        } else if (innerCategory == "gloves") {
            if (maleFemale == "men") {
                menGloves.appendTo('.variant-row');
            } else {
                womenGloves.appendTo('.variant-row');
            }

        } else if (innerCategory == "belts") {
            if (maleFemale == "men") {
                menBelts.appendTo('.variant-row');
            } else {
                womenBelts.appendTo('.variant-row');
            }

        } else if (innerCategory == "lingerie") {
            womenLingerie.appendTo('.variant-row');
        
        } else if (innerCategory == "t-shirts") {
            menShirts.hide();

        } else if (innerCategory == "jumpers" && maleFemale == "men") {
            menShirts.hide();

        } else if (innerCategory == "jackets" && maleFemale == "men") {
            menShirts.hide();

        } else if (innerCategory == "nightwear" && maleFemale == "men") {
            menShirts.hide();

        } else if (innerCategory == "coats" && maleFemale == "men") {
            menShirts.hide();

        } else if (innerCategory == "activewear" && maleFemale == "men") {
            menShirts.hide();
          
        } else {
            if (category == "clothing") {
                if (maleFemale == "men") {
                    menShirts.appendTo('.variant-row');
                } else {
                    womenClothing.appendTo('.variant-row');
                }
            }



        }
        
        checkCountrySizes();

        addSizingTooltip();

    }
    
